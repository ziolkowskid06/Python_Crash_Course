"""
Create a list of names and slice it in different places.
"""

names = ['rachel', 'alexis', 'anne', 'kendra', 'samantha', 'julia', 'kim', 'emma']
# The first three names
print(f"First three names from the list are : {names[:3]}")
# Three names from the middle
print(f"Three names from the list are: {names[4:7]}")
# The last three names
print(f"Last three names from the list are: {names[-3:]}")
