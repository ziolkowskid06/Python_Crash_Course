"""
Make a list of your favorite fruits,
and then write a series of independent if statements that check for certain fruits in your list.
"""

favorite_fruits = ['banana', 'apple', 'orange']
if 'banana' in favorite_fruits:
    print('You really like banana!')
else:
    print('Banana is not in the list')
if 'peach' in favorite_fruits:
    print('\nYou really like peach!')
else:
    print('\nPeach is not in the list.')
if 'apple' in favorite_fruits:
    print('\nYou really like apple!')
else:
    print('\nApple is not in the list.')
if 'grapes' in favorite_fruits:
    print('\nYou really like grapes!')
else:
    print('\nGrapes are not in the list.')
if 'orange' in favorite_fruits:
    print('\nYou really like orange!')
else:
    print('\nOrange is not in the list.')
